					<p>Working with TRA Robotics blurs the line between reality and science-fiction. As outsiders, we very quickly had to get up to speed with TRA’s business model as well as the inner workings of their robots and the operating process. While our laymen position initially slowed us down, it gave us the advantage of being able to instantly spot TRA’s most exciting aspects for an external audience, as we weren’t bogged down in the detail.</p>
