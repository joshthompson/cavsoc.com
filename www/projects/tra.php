					<p>Robots look cool, but some of TRA’s most exciting innovations were either digital or theoretical, and as such weren’t possible or appropriate to film on camera. We therefore took a multimedia approach to the video, utilising filmed footage, motion graphics and 3D animations to showcase TRA.</p>
					<p>We used digital glitches to stylistically transition between the animated and live-action representations of identical scenes to show the precision and forethought that went into TRA’s products.</p>
