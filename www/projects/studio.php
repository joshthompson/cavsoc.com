					<h4>Underlying concept</h4>
					<p>We wanted to create a visually striking metaphor for Kai’s recording studio, a place that both facilitates and serves as a symbol for her creative process. In this instance, it is a photography dark-room, where she processes her images.</p>
					<p>The oppressive outside world was meant as an exaggerated metaphor for the isolation that artists of all disciplines often feel – when their work is undervalued, when it is unfashionable, when it is regarded as trivial.</p>
