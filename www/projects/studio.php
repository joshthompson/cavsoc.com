					<h4>Art imitates life</h4>
					<p>Kai explained to us how she gathers inspiration from her real life experiences, takes these experiences and emotions into the studio, and creates her music from this.</p>
					<p>Likewise in the video, our outlaw photographer collects experiences in the form of images, and develops them in her dark-room. In an otherwise hostile world, this is where she feels safe and can create her art.</p>
