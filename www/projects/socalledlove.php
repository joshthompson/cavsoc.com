					<h4>An ambitious vision</h4>
					<p>We told the story of a strained relationship through the medium of interpretive dance between a domestic couple, and hinted at the mental delusion underpinning this by setting it in a surreal, otherworldly location of a living room that exists in the middle of a field. Over the course of the song, this otherworld starts to collapse, and jarring cuts to a much more sombre reality reveal the truth.</p>
