					<h4>Method to the madness</h4>
					<p>We recruited choreographer Victoria Bastable to help us tell this story, and for months spent our evenings and weekends in dance studios creating and rehearsing the routine. As we moved closer to the shoot date we moved these rehearsals to local parks so that Ebonie and Nathan could get used to dancing on an uneven, earthy surface. We made sure to include our Director of Photography Tom Allen in these rehearsals so that the flowing camera moves would feel as much a part of the choreography as the dancers themselves.</p>
