					<h4>Setting the tone</h4>
					<p>We loved both the raw emotion and disturbing twist in Ebonie’s story, but agreed that this would be too heavy handed if told as a conventional narrative. We wanted to create a concept-led piece informed by this narrative, but which could be appreciated without understanding or even being aware of the story, which recognised the uplifting vibe of the music as well as the darkness of the lyrics.</p>
